//The assignment operator (=) is used to assign a value to a variable.
let b = 2;

//The addition operator (+) adds two numbers or concatenates two strings.
const three = 1 + 2; // 3
const str = 'Hello' + ' ' + 'World'; // "Hello World"

//The subtraction operator (-) subtracts one number from another.
const two = 4 - 2; // 2

//The division operator (/) divides one number by another.
const result1 = 20 / 5; // 4
const result2 = 20 / 7; // 2.857142857142857

//The remainder operator (%) returns the remainder of a division operation.
const result3 = 20 % 5; // 0
const result4 = 20 % 7; // 6

//The multiplication operator (*) multiplies two numbers.
const result5 = 1 * 2; // 2
const result6 = -1 * 2; // -2

//The exponentiation operator (**) raises the first operand to the power of the second operand.
const result7 = 2 ** 2; // 4
const result8 = 2 ** 8; // 256